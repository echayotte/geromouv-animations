        <h4>Pilâtes</h4>
        <img class="alignleft" src="/storage/images/pages/presentation/seances-1.jpg">
        <p>
            La méthode se concentre sur les muscles profonds (acteurs essentiels de la posture et donc de l’Equilibre). Le pratiquant apprend à se centrer sur la perception de ses placements, de ses alignements, de sa respiration à la fois profonde et régulière pour des exercices fluides et dynamique. L’activité s’accompagne de quelques exercices d’équilibre et d’étirements.
        </p>
      
        <h4>Gym ludique</h4>
        <img class="alignleft" src="/storage/images/pages/presentation/seances-2.jpg">
        <p>
            Des exercices, des jeux en binôme ou en groupe pour solliciter souplesse, tonicité musculaire, équilibre... de manière distrayante. L’activité se déroule en extérieur sur le parcours santé Geromouv’ ou en intérieur (selon conditions météo).
        </p>
      
        <h4>Marche nordique douce et active</h4>
        <img class="alignleft" src="/storage/images/pages/presentation/seances-3.jpg">
        <p>
            Activité orientée sur un travail avant tout technique afin de profiter de tous les bienfaits de la marche nordique. Les bâtons permettent de se projeter vers l’avant et sollicitent l’ensemble de vos muscles (environ 80% contre 40% marche sans bâtons). L’acquisition de la technique ET la régularité feront le reste.
          
            <ul style="clear:both">
                <li>4 à 6 kms pour la marche nordique douce.</li>
                <li>6 à 8 kms pour la marche nordique active sur les chemins autour de Cahors avec des exercices d’équilibre et de renforcement musculaire pour venir compléter les <strong>« bienfaits de la marche »</strong> <a href="http://www.geromouv.fr/les-bienfaits-de-la-marche/">(article ici)</a>.</li>
            </ul>
        </p>

      
        <h4>Marche douce</h4>
        <img class="alignleft" src="/storage/images/pages/presentation/seances-4.jpg">
        <p>
            Activité organisée sur le stade Lucien Desprats. 1h40 de marche douce agrémentée d’exercices de souplesse, de renforcement musculaire, d’adresse, d’équilibre, de coordination....
        </p>
